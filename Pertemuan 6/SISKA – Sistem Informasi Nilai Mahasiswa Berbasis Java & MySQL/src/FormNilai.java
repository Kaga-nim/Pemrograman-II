    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (nimTF.getText().isEmpty() || 
            namaTF.getText().isEmpty() ||
            nil1TF.getText().isEmpty() ||
            nil2TF.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Semua field wajib diisi!");
            return;
        }
        
        try {
            Integer.parseInt(nil1TF.getText());
            Integer.parseInt(nil2TF.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Nilai harus angka!");
            return;
        }
        
        try {
            Connection conn = Koneksi.getKoneksi();
            String sql = "INSERT INTO datanilai VALUES (?, ?, ?, ?, ?)";

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, nimTF.getText());
            ps.setString(2, namaTF.getText());
            ps.setInt(3, Integer.parseInt(nil1TF.getText()));
            ps.setInt(4, Integer.parseInt(nil2TF.getText()));

            double rata = (Integer.parseInt(nil1TF.getText()) + Integer.parseInt(nil2TF.getText())) / 2.0;

            ps.setDouble(5, rata);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");

        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
        tampilData();
        clearForm();
    }//GEN-LAST:event_btnSimpanActionPerformed

