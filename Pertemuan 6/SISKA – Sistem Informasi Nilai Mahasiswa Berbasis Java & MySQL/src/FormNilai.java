    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        if (namaTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan nama untuk mencari!");
            return;
        }
        
        try {
            Connection conn = Koneksi.getKoneksi();
            Statement st = conn.createStatement();

            ResultSet rs = st.executeQuery(
                "SELECT * FROM datanilai WHERE nama LIKE '%" + namaTF.getText() + "%'"
            );

            if (rs.next()) {
                nimTF.setText(rs.getString("nim"));
                nil1TF.setText(rs.getString("nil1"));
                nil2TF.setText(rs.getString("nil2"));
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan!");
            }

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        tampilData();
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (nimTF.getText().isEmpty() || 
            namaTF.getText().isEmpty() ||
            nil1TF.getText().isEmpty() ||
            nil2TF.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Semua field wajib diisi!");
            return;
        }
        
        try {
            Integer.parseInt(nil1TF.getText());
            Integer.parseInt(nil2TF.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Nilai harus angka!");
            return;
        }
        
        try {
            Connection conn = Koneksi.getKoneksi();

            String sql = "UPDATE datanilai SET nama=?, nil1=?, nil2=?, rata=? WHERE nim=?";
            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, namaTF.getText());
            ps.setInt(2, Integer.parseInt(nil1TF.getText()));
            ps.setInt(3, Integer.parseInt(nil2TF.getText()));

            double rata = (Integer.parseInt(nil1TF.getText()) + Integer.parseInt(nil2TF.getText())) / 2.0;

            ps.setDouble(4, rata);
            ps.setString(5, nimTF.getText());

            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil diupdate!");

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        tampilData();
        clearForm();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (nimTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "NIM wajib diisi!");
            return;
        }
        
        try {
            Connection conn = Koneksi.getKoneksi();

            String sql = "DELETE FROM datanilai WHERE nim=?";
            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, nimTF.getText());
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        tampilData();
        clearForm();
    }//GEN-LAST:event_btnDeleteActionPerformed

